package model;

public class ExpedienteMedico {
	String tratamientos;
	String notas_hosp;
	String notas_quirur;
	String interconsultas;
	String repo_radiolog;
	String exa_lab;
	String notas_ambulatorias;
	public String getTratamientos() {
		return tratamientos;
	}
	public void setTratamientos(String tratamientos) {
		this.tratamientos = tratamientos;
	}
	public String getNotas_hosp() {
		return notas_hosp;
	}
	public void setNotas_hosp(String notas_hosp) {
		this.notas_hosp = notas_hosp;
	}
	public String getNotas_quirur() {
		return notas_quirur;
	}
	public void setNotas_quirur(String notas_quirur) {
		this.notas_quirur = notas_quirur;
	}
	public String getInterconsultas() {
		return interconsultas;
	}
	public void setInterconsultas(String interconsultas) {
		this.interconsultas = interconsultas;
	}
	public String getRepo_radiolog() {
		return repo_radiolog;
	}
	public void setRepo_radiolog(String repo_radiolog) {
		this.repo_radiolog = repo_radiolog;
	}
	public String getExa_lab() {
		return exa_lab;
	}
	public void setExa_lab(String exa_lab) {
		this.exa_lab = exa_lab;
	}
	public String getNotas_ambulatorias() {
		return notas_ambulatorias;
	}
	public void setNotas_ambulatorias(String notas_ambulatorias) {
		this.notas_ambulatorias = notas_ambulatorias;
	}
	
	
}
